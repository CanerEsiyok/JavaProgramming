package day07_Operators;

public class UnaryOperators {
    public static void main(String[] args) {
        int num1=25;
        int num2=-25;
        //System.out.println(num1<0);
        int a=25;
        ++a;
        System.out.println("a = " + a);
        --a;
        System.out.println("a = " + a);
        int b=100;

        System.out.println(b++);
        System.out.println("b = " + b);
    }
}
